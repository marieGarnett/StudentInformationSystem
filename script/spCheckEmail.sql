USE SIS


GO

IF OBJECT_ID('spCheckEmail') IS NOT NULL DROP PROCEDURE spCheckEmail
GO
SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO
CREATE PROCEDURE spCheckEmail @User VARCHAR(50), @Email VARCHAR(200)
AS

	DECLARE @User_ID INT

	SELECT User_ID = @User_ID
	FROM [user]
	WHERE Username = @User AND Email = @Email


GO

