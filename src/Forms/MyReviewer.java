    public void iteration(){
        try{
            String sql = "SELECT Contents FROM Content JOIN Topic ON Topic.Topic_ID = Content.Topic_ID WHERE TopicName = 'Iteration'";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                String result = rs.getString("Contents");
                jEditorPane1.setText(result);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            e.printStackTrace();
        }
    }
    
